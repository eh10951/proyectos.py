temperatura = float (input ("ingrese temperatura:"))

escala= input ("es farenheit(F) o celsius (C)?:").lower()


if escala == "f":
    celsius= (temperatura - 32) * 5/9
    print (celsius)
elif escala == "c": 
    farenheit= temperatura * 1.8 + 32
    print (farenheit)
else: print ("escala incorrecta ingresa C para celsius o F para farenheit")



